#include "Arduino.h"
#include "Timer.h"

void Timer::start(
   int delay)
{
   this->delay = delay;
   startTime = millis();
}

bool Timer::isExpired()
{
   return ((startTime != 0) &&
           ((millis() - startTime) > delay));
}
